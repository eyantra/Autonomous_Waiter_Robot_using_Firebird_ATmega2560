//Function To Initialize UART2
// desired baud rate:9600
// actual baud rate:9600 (error 0.0%)
// char size: 8 bit
// parity: Disabled
void uart0_init(void)
{
 UCSR0B = 0x00; //disable while setting baud rate
 UCSR0A = 0x00;
 UCSR0C = 0x06;
 UBRR0L = 0x47; //set baud rate lo
 UBRR0H = 0x00; //set baud rate hi
 UCSR0B = 0x98;
}

void do_work(){

	init_devices();
	float ReqdShaftCount = 0;
	unsigned long int ReqdShaftCountInt = 0;

 	ReqdShaftCount = 200 / 5.338; // division by resolution to get shaft count
 	ReqdShaftCountInt = (unsigned long int) ReqdShaftCount;
  
 	ShaftCountRight = 0;
	
	while(1)
	{

		Left_white_line = ADC_Conversion(3);	//Getting data of Left WL Sensor
		Center_white_line = ADC_Conversion(2);	//Getting data of Center WL Sensor
		Right_white_line = ADC_Conversion(1);	//Getting data of Right WL Sensor

		flag=0;	//flag=1 indicates that the required object exists
		//forward_mm(100);

		if(ShaftCountRight > ReqdShaftCountInt){
			//the bot has covered the required distance
			//turn the bot left and check for presence of object
			stop();
			servo_1(180);
			
			_delay_ms(2000);    //image processing time
			//if not found the object, then proceed
			//else
			servo_2(110); //experiment with the degrees to be turned
			_delay_ms(2000);
			servo_3(10); //grasp the object
			_delay_ms(2000);
			//go back to the front facing position
			servo_1(90);
			_delay_ms(2000);
			servo_2(90);
			_delay_ms(2000);
			
			//_delay_ms(2000);
			ShaftCountRight = 0;
		}

		if(Center_white_line>0x58)
		{
			flag=1;
			forward();
			velocity(150,150);
		}

		if((Left_white_line<0x58) && (flag==0))
		{
			flag=1;
			forward();
			velocity(130,50);
		}

		if((Right_white_line<0x58) && (flag==0))
		{
			flag=1;
			forward();
			velocity(50,130);
		}

		if(Center_white_line<0x58 && Left_white_line<0x58 && Right_white_line<0x58)
		{
			forward();
			velocity(0,0);
		}


	}

}


SIGNAL(SIG_USART0_RECV) 		// ISR for receive complete interrupt
{
	data = UDR0; 				//making copy of data from UDR0 in 'data' variable 

	UDR0 = data; 				//echo data back to PC

		if(data == 0x38) //ASCII value of 8
		{
			do_work();
		}

		if(data == 0x32) //ASCII value of 2
		{
			PORTA=0x09; //back
		}

		if(data == 0x34) //ASCII value of 4
		{
			PORTA=0x05;  //left
		}

		if(data == 0x36) //ASCII value of 6
		{
			PORTA=0x0A; //right
		}

		if(data == 0x35) //ASCII value of 5
		{
			PORTA=0x00; //stop
		}
		
		if(data == 0x72) {	//ASCII of "r"
			PORTA=0x06;  //forward
		}

		if(data == 0x37) //ASCII value of 7
		{
			buzzer_on();
		}

		if(data == 0x39) //ASCII value of 9
		{
			buzzer_off();
		}

}


